    private void tampilbibit(){
        DefaultTableModel bibit = new DefaultTableModel();
        bibit.addColumn("No");
        bibit.addColumn("id");
        bibit.addColumn("Nama bibit");
        bibit.addColumn("Stok");
        try {
            int i = 1;
            String sql = "SELECT * FROM data_bibit" ;
            java.sql.Connection connect = (Connection)koneksi.configDB();
            java.sql.Statement stm = connect.createStatement();
            java.sql.ResultSet res = stm.executeQuery(sql);
            while (res.next()) {
                bibit.addRow(new Object[]{i++,
                    res.getString(1),res.getString(2),res.getString(3)});
            }
           bibit1.setModel(bibit);
        } 
        catch (SQLException e) {
        }
    }
