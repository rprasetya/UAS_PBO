    private void txtidbibitKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtidbibitKeyPressed
        while (true){
            if(txtidbibit.getText().matches("[0-9]*")){
                if(evt.getKeyCode()==10){
                txtnamabibit.requestFocus();
                }
                break; 
            }else{
                JOptionPane.showMessageDialog(null, "Input id harus angka!");
                reset_kolom();
                txtidbibit.requestFocus();
                break;
            }
           
        } 
    }//GEN-LAST:event_txtidbibitKeyPressed

